#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	isaret;
	int	num;

	i = 0;
	num = 0;
	isaret = 1;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			isaret *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	num *= isaret;
	return (num);
}

int	main(void)
{
	char a[]	= " --+032a45";

	printf("atoi öncesi: %s\n", a);	
	printf("atoi sonrası: %d\n", ft_atoi(a));
}
